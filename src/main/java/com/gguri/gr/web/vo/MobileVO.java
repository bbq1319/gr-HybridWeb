package com.gguri.gr.web.vo;

public class MobileVO {
	
	private String token;
	private String androidID;
	private String is_mobile_aca;
	private String is_mobile_non;
	private String is_mobile_job;
	private String is_mobile_gen;
	private String type;
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getAndroidID() {
		return androidID;
	}
	public void setAndroidID(String androidID) {
		this.androidID = androidID;
	}
	public String getIs_mobile_aca() {
		return is_mobile_aca;
	}
	public void setIs_mobile_aca(String is_mobile_aca) {
		this.is_mobile_aca = is_mobile_aca;
	}
	public String getIs_mobile_non() {
		return is_mobile_non;
	}
	public void setIs_mobile_non(String is_mobile_non) {
		this.is_mobile_non = is_mobile_non;
	}
	public String getIs_mobile_job() {
		return is_mobile_job;
	}
	public void setIs_mobile_job(String is_mobile_job) {
		this.is_mobile_job = is_mobile_job;
	}
	public String getIs_mobile_gen() {
		return is_mobile_gen;
	}
	public void setIs_mobile_gen(String is_mobile_gen) {
		this.is_mobile_gen = is_mobile_gen;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
